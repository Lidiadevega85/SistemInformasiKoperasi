    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
         String no = txtNo.getText();
         String tgl = txtTgl.getText();
         String ID = txtID.getText();
         String jumlah = txtJumlah.getText();
       

        //masukkan data ke database
        connection = KoperasiDatabase.getConnection();
        String query = "INSERT INTO tbsimpanan"
                + "(No_Transaksi, Tanggal, IDAnggota, JmlSimWajib)"
                + "VALUES (?,?,?,?)";

        try {
          PreparedStatement statement= (PreparedStatement) connection.prepareStatement(query);
          
          statement.setString(1, no);
          statement.setString(2, tgl);
          statement.setString(3, ID);
          statement.setString(4, jumlah);
          
          
          statement.executeUpdate();
        } catch (SQLException ex){
          JOptionPane.showMessageDialog(this, "Terjadi error pada saat input data");
          
        } finally {
          loadData();
          reset();
        }
        
    }//GEN-LAST:event_btnsimpanActionPerformed

