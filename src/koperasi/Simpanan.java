private void initTable(){
        tableModel = new DefaultTableModel();
        tbsimpanan.setModel(tableModel);
        tableModel.addColumn("No. Transaksi");
        tableModel.addColumn("Tanggal");
        tableModel.addColumn("ID Anggota");
        tableModel.addColumn("Simpanan Wajib");
        
    }
    
    private void loadData(){
        //menghapus seluruh data yg ada di tabel
        tableModel.getDataVector().removeAllElements();
        //pemberitahuan tabel telah kosong
        tableModel.fireTableDataChanged();
        
        try{
            connection = KoperasiDatabase.getConnection();
            String query = "SELECT * FROM tbsimpanan";
            
            Statement statement = (Statement) connection.createStatement();
            ResultSet resultSet = statement.executeQuery(query);
            
            //mengisi tabel dgn data hasil query
            while (resultSet.next()){
                Object[] siswa = new Object[4];
                siswa[0] = resultSet.getString("No_Transaksi");
                siswa[1] = resultSet.getString("Tanggal");
                siswa[2] = resultSet.getString("IDAnggota");
                siswa[3] = resultSet.getString("JmlSimWajib");
                tableModel.addRow(siswa);
            }
            resultSet.close();
            statement.close();
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }
    
