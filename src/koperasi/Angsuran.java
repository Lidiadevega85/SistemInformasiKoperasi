    private void btsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsimpanActionPerformed
        // TODO add your handling code here:
         String kwitansi = txtNo.getText();
         String tgl = txttgl.getText();
         String no = txtNoPinj.getText();
         String ID = txtID.getText();
         String besar = txtBesar.getText();
         String jangka = txtjangka.getText();
         String ke = txtke.getText();
         String angsuran = txtAngsuran.getText();
         String bunga = txtBunga.getText();
         String bayar = txtBayar.getText();
       

        //masukkan data ke database
        connection = KoperasiDatabase.getConnection();
        String query = "INSERT INTO angsuran"
                + "(No_Kwitansi, Tanggal, Nomor, IDAnggota, Besar, Jangka, Ke, Angsuran, Bunga, Bayar)"
                + "VALUES (?,?,?,?,?,?,?,?,?,?)";

        try {
          PreparedStatement statement= (PreparedStatement) connection.prepareStatement(query);
          
          statement.setString(1, kwitansi);
          statement.setString(2, tgl);
          statement.setString(3, no);
          statement.setString(4, ID);
          statement.setString(5, besar);
          statement.setString(6, jangka);
          statement.setString(7, ke);
          statement.setString(8, angsuran);
          statement.setString(9, bunga);
          statement.setString(10, bayar);
         
          
          statement.executeUpdate();
        } catch (SQLException ex){
          JOptionPane.showMessageDialog(this, "Terjadi error pada saat input data");
          
        } finally {
          loadData();
          reset();
        }
    }//GEN-LAST:event_btsimpanActionPerformed

