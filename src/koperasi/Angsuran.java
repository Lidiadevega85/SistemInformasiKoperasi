private void initTable(){
        tableModel = new DefaultTableModel();
        tbangsuran.setModel(tableModel);
        tableModel.addColumn("No. Kwitansi");
        tableModel.addColumn("Tanggal");
        tableModel.addColumn("No. Pinjaman");
        tableModel.addColumn("ID Anggota");
        tableModel.addColumn("Besar Pinjaman");
        tableModel.addColumn("Ke");
        tableModel.addColumn("Angsuran Per Bulan");
        tableModel.addColumn("Bunga Per Bulan");
        tableModel.addColumn("Jumlah Bayar");
        
    }
    
    private void loadData(){
        //menghapus seluruh data yg ada di tabel
        tableModel.getDataVector().removeAllElements();
        //pemberitahuan tabel telah kosong
        tableModel.fireTableDataChanged();
        
        try{
            connection = KoperasiDatabase.getConnection();
            String query = "SELECT * FROM angsuran";
            
            Statement statement = (Statement) connection.createStatement();
            ResultSet resultSet = statement.executeQuery(query);
            
            //mengisi tabel dgn data hasil query
            while (resultSet.next()){
                Object[] siswa = new Object[10];
                siswa[0] = resultSet.getString("No_kwitansi");
                siswa[1] = resultSet.getString("Tanggal");
                siswa[2] = resultSet.getString("Nomor");
                siswa[3] = resultSet.getString("IDAnggota");
                siswa[4] = resultSet.getString("Besar");
                siswa[5] = resultSet.getString("Jangka");
                siswa[6] = resultSet.getString("Angsuran");
                siswa[7] = resultSet.getString("Ke");
                siswa[8] = resultSet.getString("Bunga");
                siswa[9] = resultSet.getString("Bayar");
                tableModel.addRow(siswa);
            }
            resultSet.close();
            statement.close();
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }
    
